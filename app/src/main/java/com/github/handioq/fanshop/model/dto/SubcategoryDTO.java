package com.github.handioq.fanshop.model.dto;

import com.google.gson.annotations.SerializedName;

public class SubcategoryDTO {

    @SerializedName("id")
    private int id;

    @SerializedName("name")
    private String name;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }
}
